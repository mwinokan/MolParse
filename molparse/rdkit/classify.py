def classify_mol(mol, draw=True):
    """Find RDKit Fragments within the molecule and draw them (or just return a list of (descriptor, count) tuples)"""

    mols = []
    data = []

    for name, d in FRAGMENT_LOOKUP.items():

        smarts = d["smarts"]
        descriptor = d["descriptor"]

        if "fragment" in d:
            matcher = d["fragment"]
            n = matcher(mol)
            if not n:
                continue
            pattern = MolFromSmarts(smarts)
            matches = mol.GetSubstructMatches(pattern, uniquify=True)

        elif "matcher" in d:
            matcher = d["matcher"]
            matches = matcher.GetMatches(mol, uniquify=True)
            if not matches:
                continue
            n = len(matches)

        else:

            pattern = MolFromSmarts(smarts)
            matches = mol.GetSubstructMatches(pattern, uniquify=True)

            if not matches:
                continue

            n = len(matches)

        mols.append(mol)

        highlight = []

        for match in matches:
            for index in match:
                highlight.append(index)

        data.append((descriptor, n, highlight))

    data = sorted(data, key=lambda x: (-len(x[2]), -x[1]))

    if draw:
        legends = [f"{n} x {descriptor}" for descriptor, n, _ in data]
        drawing = Draw.MolsToGridImage(
            mols,
            highlightAtomLists=[highlights for _, _, highlights in data],
            legends=legends,
        )
        display(drawing)
    else:
        return counts


